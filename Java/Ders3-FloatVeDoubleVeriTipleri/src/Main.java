public class Main {
    public static void main(String[] args) {
        float vFloat = 1; // float 32 bit ve cok buyuk araliktadir, ondalikli sayilar
        System.out.println(vFloat);

        float x = 3.14F; // veya 3.14f
        System.out.println(x);

        double vDouble = 3.14; // double 64 bit ve cok buyuk aralik, en cok kullanilan
        System.out.println(vDouble);

        double vDouble2 = 3;
        System.out.println(vDouble2);
    }
}
