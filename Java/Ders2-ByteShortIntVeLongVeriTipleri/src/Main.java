public class Main {
    public static void main(String[] args) {
        byte vByte = 100; // byte 8 bit -128-127
        System.out.println(vByte);

        short vShort = 1000; // short 16 bit -32.768-32.767
        System.out.println(vShort);

        int vInt = 10000; // int 32 bit -2.147.483.648-2.147.483.647
        System.out.println(vInt);

        long vLong = 10000000; // long 64 bit ve cok buyuk degerler arasindadir
        System.out.println(vLong);
    }
}
